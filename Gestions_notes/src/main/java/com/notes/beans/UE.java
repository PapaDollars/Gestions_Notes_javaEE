package com.notes.beans;

public class UE {
	
	private String id_ue ;
	private String code_UE ;
	private String titre ;
	private String credit ;
	
	
	public String getId_ue() {
		return id_ue;
	}
	public void setId_ue(String id_ue) {
		this.id_ue = id_ue;
	}
	public String getCode_UE() {
		return code_UE;
	}
	public void setCode_UE(String code_UE) {
		this.code_UE = code_UE;
	}
	public String getTitre() {
		return titre;
	}
	public void setTitre(String titre) {
		this.titre = titre;
	}
	public String getCredit() {
		return credit;
	}
	public void setCredit(String credit) {
		this.credit = credit;
	}

	
	
}
