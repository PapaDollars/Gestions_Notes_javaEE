package com.notes.beans;

public class Etudiants {
	private String id_etudiant;
	private String matricule;
	private String nom;
	private String prenom;
	private String sexe;
	private String age;
	private String telephone;
	private String adresse;
	private String id_note;
	
	
	public String getId_etudiant() {
		return id_etudiant;
	}
	public void setId_etudiant(String id_etudiant) {
		this.id_etudiant = id_etudiant;
	}
	public String getMatricule() {
		return matricule;
	}
	public void setMatricule(String matricule) {
		this.matricule = matricule;
	}
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public String getPrenom() {
		return prenom;
	}
	public void setPrenom(String prenom) {
		this.prenom = prenom;
	}
	public String getSexe() {
		return sexe;
	}
	public void setSexe(String sexe) {
		this.sexe = sexe;
	}
	public String getId_note() {
		return id_note;
	}
	public void setId_note(String id_note) {
		this.id_note = id_note;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getTelephone() {
		return telephone;
	}
	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}
	public String getAdresse() {
		return adresse;
	}
	public void setAdresse(String adresse) {
		this.adresse = adresse;
	}
	
	
}
